#include "libft.h"

int	ft_atoi(const char *nptr)
{
	unsigned int	nbr;
	int				res;
	int				sign;

	nbr = 0;
	res = 0;
	sign = 1;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -sign;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		nbr = *nptr - '0';
		res = res * 10 + nbr;
		nptr++;
	}
	res *= sign;
	return (res);
}
